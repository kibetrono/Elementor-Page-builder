<h1>Shortcode</h1>
<p> use shortcode to display the social share icons  in the contents i.e [socialShareShortcode]</p>

<h1>Social Share</h1>
<h2>Social Networks</h2>
<p>In this tab you can choose which social media icons you want to display in the frontend of your site.</p>
<h2>Share Options</h2>

<p>In this tab you can select the options where you want to display social media share icons e.g pages or on posts</p>

<h1>How To Display</h1>
<h2>Button sizes</h2>
<p>In this tab you can choose the button size of social media icons to display i.e either small, medium or large</p>
<h2>Social Network Color</h2>
<p>In this tab you can choose the color for the icons. A default option displays with a white background while the color picker option allows you to choose your own color</p>
<h2>Button Order</h2>
<p>In this tab you have an option of choosing an order in which the icons will appear either starting from facebook to whatsapp or start with whatsapp to facebook</p>
<h2>Display Position</h2>
<p>In this tab you have an option to display the area in which your social icons will appear.This includes four options:</p>
<ul>
    <li>Before the post content.</li>
    <li>After the post content.</li>
    <li>Below the post title.</li>
    <li>Floating on the left area.</li>
    <li>Inside the featured image.</li>
</ul>